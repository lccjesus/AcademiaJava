package qintess.academiajava.variaveis.inteiros;

public class VariaveisInteiras01 {
	public static void main(String[] args) {
		// variáveis byte: permitem valores entre -128 a 127
		byte tamanho = 120;
		
		// Variáveis short: entre -32768 a 32767
		short alunos = 1500;
		
		// Variáveis int: entre -2147483648 a 2147483647
		int populacao = 650000;
		
		System.out.println("Tamanho informado: " + tamanho);
		System.out.println("Número de alunos: " + alunos);
		System.out.println("População da minha cidade: " + populacao);
		System.out.println("Teste do BYTE: " + tamanho);
		
		
	}

}
